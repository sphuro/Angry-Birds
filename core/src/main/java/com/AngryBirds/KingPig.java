public class KingPig extends Main implements Screen,Materials {
    private SpriteBatch batch;
    private OrthographicCamera camera;
    private Stage stage;
    private Viewport viewport;
    private Main game;
    private Texture kingpig;
    float x,y,width,height;

    public KingPig(Main game,float x,float y,float width,float height) {
        this.game = game;
        batch = new SpriteBatch();
        camera = new OrthographicCamera();
        viewport = new StretchViewport(1600, 900, camera);
        stage = new Stage(viewport, batch);
        kingpig = new Texture("kingpig.png");
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    @Override
    public void show() {

    }

    public void draw() {
        batch.setProjectionMatrix(camera.combined);
        viewport.apply();
        batch.begin();
        batch.draw(kingpig, x, y, width, height);
        batch.end();
    }

    @Override
    public void render(float delta) {

    }

    @Override
    public void resize(int i, int i1) {
        viewport.update(i, i1);
        camera.position.set(viewport.getWorldWidth() / 2 , viewport.getWorldHeight() / 2, 0);
        camera.update();
        batch.setProjectionMatrix(camera.combined);
    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void dispose() {
        batch.dispose();
        stage.dispose();
    }
}
